// Create a program that converts a temperature from Celsius to Fahrenheit. 
// Declare a variable `celsius` and assign it with a numeric value 
// representing the temperature in Celsius. 
// Use the formula `(celsius * 9/5) + 32` to calculate the equivalent 
// temperature in Fahrenheit. Increment the result by 1 using ++ operator. Print the converted temperature to the console.
let celsius = 28;
let fahrenheit = (celsius * 9/5) + 32;
fahrenheit++
console.log("The temperature in Fahrenheit is: " + fahrenheit);
