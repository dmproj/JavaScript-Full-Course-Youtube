//BINARY
//addition (+), subtraction (-), multiplication (*), //division (/)
//and comparison operators like greater than (>) and less than (<).
let multiply = 2 * 3;
console.log(multiply); // Output: 6

let devide = 6 / 3;
console.log(devide); // Output: 2


//UNARY
//unary plus (+), unary minus (-), increment (++), and decrement (--)
let negation = 10;
let result = -negation;
console.log(result); // Output: -10 (negation)

let increment = 5;
increment++;
console.log(increment); // Output: 6

//OPERAND
//An operand is a value or expression that an operator operates on.
let a = 5; // 'a' is an operand
let b = 3; // 'b' is an operand

let sum = a + b; // '+' is a binary operator, 'a' and 'b' are operands
console.log(sum); // Output: 8









